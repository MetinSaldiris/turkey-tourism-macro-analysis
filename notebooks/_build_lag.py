    "def yoy_log_safe(s):\n"
    "    s = s.replace(0, np.nan)\n"
    "    return np.log(s) - np.log(s.shift(12))\n"
    "\n"
    "yoy_arrivals = yoy_log_safe(df['arrivals_total'])\n"
    "\n"
    "yoy_results = {}\n"
    "fig, axes = plt.subplots(1, 3, figsize=(18, 4.5))\n"
    "for ax, mkt in zip(axes, ['DE', 'GB', 'RU']):\n"
    "    spec = MARKETS[mkt]\n"
    "    yoy_trends = yoy_log_safe(df[spec['trends']])\n"
    "    lags, vals, conf, n = ccf_two_sided(yoy_trends, yoy_arrivals)\n"
    "    plot_ccf(ax, lags, vals, conf, n,\n"
    "             f'{mkt} — YoY Trends vs YoY arrivals', spec['color'])\n"
    "    yoy_results[mkt] = (lags, vals, conf, n)\n"
    "fig.tight_layout()\n"
    "fig.savefig(FIG_DIR / 'lag_yoy_ccf.png', dpi=150)\n"
    "plt.show()\n"
    "\n"
    "yoy_rows = []\n"
    "for mkt, (lags, vals, conf, n) in yoy_results.items():\n"
    "    absvals = np.abs(vals)\n"
