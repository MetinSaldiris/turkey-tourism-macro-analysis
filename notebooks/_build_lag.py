# Task 6 — YoY CCF section.
cells.append(nbf.v4.new_markdown_cell(
    "## YoY CCF (seasonally adjusted lead/lag)\n"
    "\n"
    "The raw CCF above measures the shared annual cycle; this section repeats "
    "the analysis on $\\Delta_{12} \\log$ changes so the remaining correlation "
    "reflects deviations from each series' typical seasonal path (i.e., the "
    "part of arrivals not explained by \"it is July\")."
))

