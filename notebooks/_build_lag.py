    "lags_post, vals_post, conf_post, n_post = ccf_two_sided(post[ru_col], post['arrivals_total'])\n"
    "\n"
    "fig, axes = plt.subplots(1, 2, figsize=(13, 4.5))\n"
    "plot_ccf(axes[0], lags_pre,  vals_pre,  conf_pre,  n_pre,\n"
